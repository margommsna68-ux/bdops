// ═══ Translation function ═══
export function t(key: string, lang?: AppLanguage): string {
  const l = lang ?? useSettingsStore.getState().language;
  return translations[key]?.[l] ?? translations[key]?.en ?? key;
}

// ═══ React hook for translations (reactive to language changes) ═══
export function useT() {
  const language = useSettingsStore((s) => s.language);
  return (key: string): string => {
    return translations[key]?.[language] ?? translations[key]?.en ?? key;
  };
}
